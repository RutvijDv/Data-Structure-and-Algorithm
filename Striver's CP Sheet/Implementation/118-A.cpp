void solve()
{
    string s;
    cin >> s;

    int i = 0;

    while (i < s.size())
    {
        char x = s[i];

        if (x == 'a' || x == 'A' || x == 'e' || x == 'E' || x == 'i' || x == 'I' || x == 'o' || x == 'O' || x == 'u' || x == 'U' || x == 'y' || x == 'Y')
        {
            s.erase(i, 1);
        }
        else
        {
            s.insert(i, ".");
            i++;
            if (x < 95)
            {
                s[i] = s[i] + 32;
            }

            i++;
        }
    }

    cout << s << endl;
}

